package miniSephora;

public class User {
	
	String name;
	String surname;
	String username;
	String password;
	String email;
	String phoneNumber;
	
	public User(String name, String surname, String username, String password, String email, String phoneNumber) {
		this.name = name;
		this.surname = surname;
		this.username = username;
		this.password = password;
		this.email = email;
		this.phoneNumber = phoneNumber;
	}
}
